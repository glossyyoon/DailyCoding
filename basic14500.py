n, m = map(int, input().split())
a = [list(map(int, input().split())) for _ in range(n)]
ans = 0
position = (((0, 1), (0, 2), (0, 3)), ((1, 0), (2, 0), (3, 0)),
            ((0, 1), (1, 0), (1, 1)), ((0, 1), (1, 1), (1, 2)),
            ((1, 0), (1, -1), (0, 1)), ((1, 0), (1, -1), (2, -1)),
            ((1, 0), (1, 1), (2, 1)), ((0, 1), (0, 2), (1, 2)),
            ((1, 0), (1, -1), (1, -2)), ((1, 0), (0, 1), (0, 2)),
            ((1, 0), (1, 1), (1, 2)), ((1, 0), (2, 0),
                                       (2, 1)), ((0, 1), (1, 0), (2, 0)),
            ((1, 0), (2, 0), (2, -1)), ((0, 1), (1, 1),
                                        (2, 1)), ((1, 0), (2, 0), (1, 1)),
            ((1, 0), (2, 0), (1, -1)), ((0, 1), (0, 2),
                                        (1, 1)), ((1, 0), (1, -1), (1, 1)))
for i in range(n):
    for j in range(m):
        for block in position:
            ok = True
            s = a[i][j]
            for dx, dy in block:
                x, y = i + dx, j + dy
                if 0 <= x < n and 0 <= y < m:
                    s += a[x][y]
                else:
                    ok = False
            if ans < s and ok:
                ans = s
print(ans)